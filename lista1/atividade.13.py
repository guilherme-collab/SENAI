primeira_nota = float(input("Digite a primeira nota: "))
#pede ao cliente que insira a primeira e segunda nota
segunda_nota = float(input("Digite a segunda nota: "))
#calculo da média ponderada e o resultado
media_ponderada = (primeira_nota * 2 + segunda_nota * 3) / 5
print("a média ponderada é de: ",media_ponderada)
