raio= float(input("Digite qual o raio da caixa d'água cilíndrica: ")) #pede ao cliente que insira o raio da caixa 
altura = float(input("Digite qual a altura da caixa d'água cilíndrica: ")) #pede que insira a altura da caixa d agua
calculo = 3.14 * (raio * raio) * altura #area do calculo
print(f"o volume dessa caixa da agua cilindrica é de {calculo}") #exibe o resultado 